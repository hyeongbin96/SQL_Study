SELECT
    CAR_ID,
    CAR_TYPE,
    DAILY_FEE,
    OPTIONS
FROM
    CAR_RENTAL_COMPANY_CAR
WHERE 
    OPTIONS LIKE '%네비게이션%'
ORDER BY 
    CAR_ID DESC

SELECT
    CAR_ID,
    CAR_TYPE,
    DAILY_FEE,
    OPTIONS
FROM
    CAR_RENTAL_COMPANY_CAR
WHERE 
    OPTIONS REGEXP '네비게이션'
ORDER BY 
    CAR_ID DESC

SELECT
    CAR_ID,
    CAR_TYPE,
    DAILY_FEE,
    OPTIONS
FROM
    CAR_RENTAL_COMPANY_CAR
WHERE 
    INSTR(OPTIONS, '네비게이션') > 0
ORDER BY 
    CAR_ID DESC

