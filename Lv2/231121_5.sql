SELECT
    CASE 
        WHEN PRICE BETWEEN 0 AND 9999 THEN 0
        WHEN PRICE BETWEEN 10000 AND 19999 THEN 10000
        WHEN PRICE BETWEEN 20000 AND 29999 THEN 20000
        WHEN PRICE BETWEEN 30000 AND 39999 THEN 30000
        WHEN PRICE BETWEEN 40000 AND 49999 THEN 40000
        WHEN PRICE BETWEEN 50000 AND 59999 THEN 50000
        WHEN PRICE BETWEEN 60000 AND 69999 THEN 60000
        WHEN PRICE BETWEEN 70000 AND 79999 THEN 70000
        WHEN PRICE BETWEEN 80000 AND 89999 THEN 80000
    END AS PRICE_GROUP,
    COUNT(*) AS PRODUCTS
FROM
    PRODUCT
GROUP BY
    PRICE_GROUP
ORDER BY
    PRICE


################## 위처럼하면 테스트 효율성 개박살남

# 1. FLOOR = 소수점 이하 값을 버리고 가장 크지 않은 정수 값 반환
SELECT
    FLOOR(PRICE / 10000) * 10000 AS PRICE_GROUP,
    COUNT(PRICE) AS PRODUCTS
FROM
    PRODUCT
GROUP BY
    PRICE_GROUP
ORDER BY
    PRICE ASC

# 2. TRUNCATE = number를 소수점 아래 decimals 번째까지 잘라내는 역할 ex)TRUNCATE(12.3456, 2) = 12.34
SELECT
    (TRUNCATE(PRICE / 10000, 0) * 10000) AS PRICE_GROUP,
    COUNT(PRICE) AS PRODUCTS
FROM
    PRODUCT
GROUP BY
    PRICE_GROUP
ORDER BY
    PRICE ASC

# 3. 간단하게 아래처럼도 가능
SELECT
    PRICE - (PRICE % 10000) AS PRICE_GROUP,
    COUNT(PRICE) AS PRODUCTS
FROM
    PRODUCT
GROUP BY
    PRICE_GROUP
ORDER BY
    PRICE ASC